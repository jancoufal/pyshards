def part1(game_input):
	captcha = list(game_input)
	captcha.append(captcha[0])

	p = -1
	acc = 0

	for digit in captcha:
		if p == digit:
			acc += digit
		p = digit

	print(acc)


def part2(game_input):
	captcha = list(game_input)
	captcha_len = len(captcha)
	shift = int(captcha_len / 2)

	acc = 0
	for index, digit in enumerate(captcha):
		if digit == captcha[(index + shift) % captcha_len]:
			acc += digit

	print(acc)


